    private void jButton5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton5MouseClicked
        if (cantidad != 0) {
            llenado += jTextField5.getText() + ",";
            cont_llenado++;
            if (cont_llenado == cantidad) {
                jTextField4.setEnabled(true);
                mat[cont_alf][cont_est] = llenado;
                llenado = "";
                cont_llenado = 0;
                jTextField5.setEnabled(false);
                cont_alf++;
                if (cont_alf == tam_alf + 1) {
                    if (cont_est == no_estados - 1) {
                        jLabel5.setVisible(false);
                        jLabel6.setVisible(false);
                        jTextField4.setVisible(false);
                        jButton3.setVisible(false);
                        cont_alf = 0;
                        cont_est = 0;

                        jLabel7.setVisible(true);
                        jRadioButton2.setVisible(true);
                        jButton6.setVisible(true);
                        jLabel7.setText("q" + cont_est);

                    } else {
                        cont_est++;
                        cont_alf = 0;
                        jLabel5.setText("q" + cont_est);

                    }
                }
                jLabel6.setText(Character.toString(vec_alf[cont_alf]));
            }
        }
        else {
            jTextField4.setEnabled(true);
            mat[cont_alf][cont_est] = "/";
            llenado = "";
            cont_llenado = 0;
            jTextField5.setEnabled(false);
            cont_alf++;
            if (cont_alf == tam_alf + 1) {
                if (cont_est == no_estados - 1) {
                    jLabel5.setVisible(false);
                    jLabel6.setVisible(false);
                    jTextField4.setVisible(false);
                    jButton3.setVisible(false);
                    cont_alf = 0;
                    cont_est = 0;

                    jLabel7.setVisible(true);
                    jRadioButton2.setVisible(true);
                    jButton6.setVisible(true);
                    jLabel7.setText("q" + cont_est);

                } else {
                    cont_est++;
                    cont_alf = 0;
                    jLabel5.setText("q" + cont_est);

                }
            }
            jLabel6.setText(Character.toString(vec_alf[cont_alf]));
            
        }
        
    }//GEN-LAST:event_jButton5MouseClicked

