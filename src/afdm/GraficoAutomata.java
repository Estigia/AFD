    public String desplegarMatriz(int mtx[][])
    {
        jTextArea1.setVisible(true);
        String matriz = "\t";
        for (int i = 0; i < abc.length; i++) {
            matriz = " "  + matriz + abc[i] + "\t";
        }
        matriz += "\n";
        for (int i = 0; i < (alto_automata); i++) {
            matriz = matriz + "Q" + i + "-->" + "\t";
            for (int j = 0; j < ancho_automata-1; j++) {
                matriz = matriz + "Q" + automata[i][j] + "\t";
            }
            matriz += "\n";
        }
        
        return matriz;   
    }
    
    public void construirAutomata()
    {
        GraphViz graph = new  GraphViz();
        graph.addln(graph.start_graph());
        for (int i = 0; i < alto_automata; i++) {
            if(automata[i][0] == -1){}
            else {
                if (automata[i][ancho_automata - 1] == 1) {
                    graph.addln("q" + i + " [shape = doublecircle];");
                }
                for (int j = 0; j < ancho_automata - 1; j++) {
                    graph.addln("q" + i + " -> " + "q" + automata[i][j] + " [label = " + "\"" + abc[j] + "\" " + "];");
                }
            }
        }        
        graph.addln(graph.end_graph());
        System.out.println(graph.getDotSource());
        jTextArea2.append(graph.getDotSource());
        File out = new File("out.gif");
        graph.writeGraphToFile(graph.getGraph(graph.getDotSource()), out);
    }

